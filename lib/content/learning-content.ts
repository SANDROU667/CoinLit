function mapLessons(seeds: LessonSeed[], minutes: number): CourseLesson[] {
  return seeds.map((seed) => ({
    ...seed,
    minutes
  }));
}

function enrichTheoryText(seed: LessonSeed, levelLabel: string) {
  return [
    seed.material,
    `Теоретическая страница 2 (${levelLabel}): разложи идею на структуру "контекст рынка -> точка входа -> ограничение риска -> критерий выхода -> пост-анализ".`,
    `Теоретическая страница 3 (${levelLabel}): зафиксируй метрики качества решения, отметь эмоциональные триггеры и выпиши действия, которые помогают сохранить дисциплину в волатильной фазе.`
  ].join("\n\n");
}

function expandLessonSeries(seeds: LessonSeed[], targetCount: number, courseId: string, minutes: number): CourseLesson[] {
  return Array.from({ length: targetCount }, (_, index) => {
    const source = seeds[index % seeds.length];
    const chapter = Math.floor(index / seeds.length) + 1;
    const lessonNumber = index + 1;
    const variant = (index % 3) + 1;

    return {
      ...source,
      id: `${courseId}-${String(lessonNumber).padStart(2, "0")}`,
      title: `${source.title} • модуль ${chapter}.${variant}`,
      material: enrichTheoryText(source, `${courseId.toUpperCase()} / урок ${lessonNumber}`),
      practice: `${source.practice} Дополнительно: оформи короткий отчет по уроку ${lessonNumber} в формате "что понял -> что проверил -> что применю".`,
      keyPoints: [
        ...source.keyPoints,
        "Фиксируй решение до входа, а не после движения цены.",
        "Отмечай рыночный сленг и проверяй термины через подсказки."
      ],
      minutes
    };
  });
}

